public class CalculadoraSimple {
    
    public CalculadoraSimple(){}
    
    public int sumar(int n1, int n2){
        return n1 + n2;
    }
    
    public int restarr(int n1, int n2){
        return n1 - n2;
    }
    
    public int multiplicar(int n1, int n2){
        return n1 * n2;
    }
    
    
}
